        <p class= "article">1809: <b>Humphry Davy</b> coloca una fina tira de carbón entre los dos polos de una pila. Se considera el origen de la bombilla.</p><br>
        <img src="https://upload.wikimedia.org/wikipedia/commons/thumb/3/38/Davy_lamp.png/250px-Davy_lamp.png" width="250" height="300 " alt="Lampara davy" style="vertical-align:center">
        <p class="article">1840: <b>Warren de la Rue</b> coloca un filamento de platino en el interior de un tubo de vacío. Logra aumentar la duración de la luz, pero sus costes eran excesivos.</p><br>
        <img src="https://s3.amazonaws.com/s3.timetoast.com/public/uploads/photo/7775301/image/4b323de05f9806bd07fe7d31a15ce367"alt="warren de la rue light bulb > Off-75%" style="width: 360px; height:480px;">
        <p class="article">1875: <b>Henry Woodward y Matthew Evans</b> se hacen la patente de la bombilla, que era básicamente la misma que hacía tres décadas.</p><br>
        <img src="https://www.delmarfans.com/images/content/educate/History_of_Light_Bulb/1874_henrywoodwardmatheweva.jpg" width="600" height="167" alt="Lampara davy" style="vertical-align:center">
        <p class="article">1879: <b>Joseph Wilson Swan</b>  presenta una bombilla de cristal sin aire, cables de platino y un elemento emisor de luz de carbono, que no duraba demasiado en una instalación real.</p><br>
        <img src="https://www.wired.com/images_blogs/thisdayintech/2009/12/swan-lamp.jpg" width="640" height="508 " alt="Lampara davy" style="vertical-align:center">
        <p class="article">1880: <b>Thomas Edison</b>, tras comprar la patente de Woodward y Evans, presenta una bombilla con un filamento de bambú carbonatado que llegaba a las 600 horas de duración. Nace el primer modelo realmente comercial.</p><br>
        <img src="https://rinconeducativo.org/sites/default/files/edison_thomas_alva_0.jpg" jsaction="load:XAeZkd;" jsname="HiaYvf" class="n3VNCb" alt="Thomas Alva Edison, el hombre de los más de 1.000 inventos | Rincón  Educativo" data-noaft="1" style="width: 383px; height: 206.351px;">
